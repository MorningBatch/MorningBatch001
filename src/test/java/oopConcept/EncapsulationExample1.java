package oopConcept;

public class EncapsulationExample1 {

	private String name = "Shareef";

	private int age = 31;

	private float height = 6.1f;

	public void getName() {
		System.out.println(name);
	}

	public void getAge() {
		System.out.println(age);
	}

	public void getHeight() {
		System.out.println(height);
	}

}
