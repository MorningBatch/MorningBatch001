package com.JavaPractice.org;

public class DataTypesExample1 {

	boolean a;
	char c;
	byte b;
	short s;
	int i;
	long l;
	float f;
	double d;
		
	public static void main(String[] args) {

		DataTypesExample1 dte = new DataTypesExample1();

		System.out.println(" boolean default value is : " +dte.a);
		System.out.println(" char default value is : " +dte.c);
		System.out.println(" byte default value is : " +dte.b);
		System.out.println(" short default value is : " +dte.s);
		System.out.println(" int default value is : " +dte.i);
		
		System.out.println(" long default value is : " +dte.l);
		System.out.println(" float default value is : " +dte.f);
		System.out.println(" double default value is : " +dte.d);
		
	}

}
