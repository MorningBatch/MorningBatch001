package com.JavaPractice.org;

public class UnaryOperatorExample2 {

	public static void main(String[] args) {

		int a = 10;
		int b;
		System.out.println(a);

		b = ++a;
		System.out.println(b);

		b = a++;
		
		System.out.println(b);

		b = a++;
		System.out.println(b);

		b = --a;
		System.out.println(b);

		b = a--;
		System.out.println(b);

		b = a--;
		System.out.println(b);

	}

}
